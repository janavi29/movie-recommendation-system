def convert(obj):
  L = []
  if isinstance(obj, str) and obj.startswith('[') and obj.endswith(']'):
    for i in ast.literal_eval(obj):
      L.append(i['name'])
  return L

movies['genres'] = movies['genres'].apply(convert)

