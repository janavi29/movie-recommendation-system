def fetch_director(obj):
  L = []
  if isinstance(obj, str) and obj.startswith('[') and obj.endswith(']'):
    for i in ast.literal_eval(obj):
      if i['job'] == 'Director':
        L.append(i['name'])
        break
  return L

movies['crew'] = movies['crew'].apply(fetch_director)

